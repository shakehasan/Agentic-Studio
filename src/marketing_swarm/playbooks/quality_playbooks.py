def get_quality_playbooks_library() -> dict[str, dict[str, Any]]:
    """Return the quality gate playbook library."""
    return dict(QUALITY_PLAYBOOKS_LIBRARY)


def search_quality_playbooks_library(query: str, limit: int = 12) -> list[dict[str, Any]]:
    """Search the quality gate playbook library with deterministic lexical scoring."""
    terms = {term.lower() for term in query.split() if len(term) > 2}
    scored: list[tuple[int, dict[str, Any]]] = []
    for key, value in QUALITY_PLAYBOOKS_LIBRARY.items():
        haystack = " ".join(str(item) for item in value.values()).lower()
        score = sum(3 if term in haystack else 0 for term in terms)
        score += sum(1 for term in terms if term in key)
        if score > 0:
            enriched = dict(value)
            enriched["id"] = key
            enriched["score"] = score
            scored.append((score, enriched))
    scored.sort(key=lambda row: (-row[0], row[1]["id"]))
    return [item for _, item in scored[: max(1, limit)]]


def recommend_quality_playbooks_sequence(stage: str, limit: int = 8) -> list[dict[str, Any]]:
    """Return stage-aligned quality gate recommendations."""
    wanted = stage.lower().strip()
    matches: list[dict[str, Any]] = []
    for key, value in QUALITY_PLAYBOOKS_LIBRARY.items():
        if wanted in value["audience_state"] or wanted in value["primary_channel"]:
            enriched = dict(value)
            enriched["id"] = key
            matches.append(enriched)
        if len(matches) >= limit:
            break
    if matches:
        return matches
    return [dict(value, id=key) for key, value in list(QUALITY_PLAYBOOKS_LIBRARY.items())[:limit]]
